# 1. 复制此文件内容到 ~/.zshrc 或 ~/.bashrc
# 2. 填入你的实际 API 密钥
# 3. 运行 source ~/.zshrc 使配置生效
#
# 这样每次克隆项目后就不需要重新填写密钥了

